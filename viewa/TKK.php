      function xemtkkhoa() {
    $.ajax({
        url: './api/apia.php?action=xemtkkhoa',
        type: 'GET',
        dataType: 'json', // Tự động phân tích chuỗi JSON thành object/mảng
        success: function(response) {
            if (Array.isArray(response) && response.length > 0) {
                var events = response;
                var eventHtml = '';
                events.forEach(function(event) {
                    eventHtml += `
                     
                      <tr>
                    <td>${event.id}</td>
                    <td>${event.Name}</td>
                    <td>${event.Email}</td>
                    <td>${event.sdt}</td>
                    <td>${event.Address}</td>
                    <td>${event.Datetime}</td>
                    <td><img src="assets/img/user/${event.profile}" alt="Avatar" width=40px height=40px class="avatar mb-3"></td>`;
                    
                     eventHtml +=`<td>
                        <div class="action-buttons">
                           
                            <button class="btn delete" onclick="mokhoa('${event.id}')">Mở khóa</button>
                        </div>
                    </td>
                </tr> 
`;
                });
                $('#employee-table').html(eventHtml);
            } else {
                $('#employee-table').html('<div class="col">Không tìm thấy thông tin người dùng.</div>');
            }
        },
        error: function(xhr, status, error) {
            console.error('Lỗi khi lấy thông tin:', error);
            $('#employee-table').html('<div class="col">Đã xảy ra lỗi khi tải thông tin người dùng.</div>');
        }
    });
}


function mokhoa(id) {
       
       fetch('./api/apia.php?action=mokhoa&id=' + id)
           .then(response => response.text())
          
           .then(data => {
               
               if (data === 'gui') {
                   // Chuyển hướng người dùng sau khi cập nhật thành công
                   openPopup('Mở khóa thành công', '');
                   setTimeout(function() {
                       window.location.href = 'indexa.php?TKK';
                   }, 1000);
               } else {
                   openPopup('Mở khóa không thành công','');
               }
           })
           .catch(error => console.error('Lỗi:', error));
}

$(document).ready(function() {
       
       xemtkkhoa();
       
   });
</script>
