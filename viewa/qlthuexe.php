    function applyResponsiveTableHeaders() {
  const table = document.querySelector('table');
  const headers = Array.from(table.querySelectorAll('thead th'));
  const rows = table.querySelectorAll('tbody tr');

  rows.forEach(row => {
    const cells = row.querySelectorAll('td');
    cells.forEach((cell, index) => {
      if (headers[index]) {
        cell.setAttribute('data-header', headers[index].innerText);
      }
    });
  });
}

document.addEventListener('DOMContentLoaded', applyResponsiveTableHeaders);

 
           function loadRentals1(){
    $.ajax({
        url: './api/apia.php?action=xemxethuenv',
        type: 'GET',
        dataType: 'json', // Tự động phân tích chuỗi JSON thành object/mảng
        success: function(response) {
            if (Array.isArray(response) && response.length > 0) {
                var events = response;
                var eventHtml = '';
                events.forEach(function(event) {
                    eventHtml += `
                     
                     <tr>
                                <td>${event.rental_id}</td>
                                <td>${event.customer_name}</td>
                                <td>${event.customer_phone}</td>
                                <td>${event.vehicle_type}</td>
                                <td>${event.name}</td>
                                <td>${event.pickup_time}</td>
                                <td>${event.pickup_location}</td>
                                <td>${event.dropoff_location}</td>
                               
                           
                  
                    `;
                if(event.Trangthai == 0)
                {
                    eventHtml +=`<td style="color:red">Chưa xác nhận</td> `;
                }else if(event.Trangthai == 1){
                    eventHtml +=`<td style="color:green">Đã xác nhận</td> 
                    `;
                }                   
                eventHtml +=`<td> <button class="btn edit" onclick="xacnhanxe('${event.rental_id}')">✔</button></td></tr>
                `;
                });
                $('#employee-table').html(eventHtml);
                applyResponsiveTableHeaders();
            } else {
                $('#employee-table').html('<div class="col">Không tìm thấy thông tin người dùng.</div>');
            }
        },
        error: function(xhr, status, error) {
            console.error('Lỗi khi lấy thông tin:', error);
            $('#employee-table').html('<div class="col">Đã xảy ra lỗi khi tải thông tin người dùng.</div>');
        }
    });
}
function xacnhanxe(id) {
       
       fetch('./api/apia.php?action=xacnhanxe&id=' + id)
           .then(response => response.text())
          
           .then(data => {
               
               if (data === 'gui') {
                   // Chuyển hướng người dùng sau khi cập nhật thành công
                   openPopup('Xác nhận thành công', '');
                   setTimeout(function() {
                       window.location.href = 'indexa.php?qlthuexe';
                   }, 1000);
               } else {
                   openPopup('Xác nhận không thành công','');
               }
           })
           .catch(error => console.error('Lỗi:', error));
}
